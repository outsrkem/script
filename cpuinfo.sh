#!/bin/bash
export  LANG=en_US.UTF-8 && \
lscpu |egrep '^Model\ name:'&& \
lscpu |egrep '^Socket' && \
lscpu |egrep '^Core'&& \
lscpu |egrep '^Thread'


cat << EOF 

┌─────────────────────┬─────────────────────────────────────────────────────┐
│ Socket(s)           │  物 理 CPU 个 数                                    │
├─────────────────────┼─────────────────────────────────────────────────────┤
│ Core(s) per socket  │  物 理 CPU 个 数                                    │
├─────────────────────┼─────────────────────────────────────────────────────┤
│ Thread(s) per core  │  每 个 核 心 的 线 程 数                            │
├─────────────────────┴─────────────────────────────────────────────────────┤
│ 对 操 作 系 统 来 说，其 逻 辑 CPU 的 数 量 就 是 Socket*Core*Thread      │
└───────────────────────────────────────────────────────────────────────────┘
EOF
